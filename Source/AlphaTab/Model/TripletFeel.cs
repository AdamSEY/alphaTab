namespace AlphaTab.Model
{
    // ReSharper disable InconsistentNaming
    /// <summary>
    /// This public enumeration lists all feels of triplets.
    /// </summary>
    public enum TripletFeel
    {
        /// <summary>
        /// No triplet feel
        /// </summary>
        NoTripletFeel, 
        /// <summary>
        /// Triplet 16th
        /// </summary>
        Triplet16th,
        /// <summary>
        /// Triplet 8th
        /// </summary>
        Triplet8th,
        /// <summary>
        /// Dotted 16th
        /// </summary>
        Dotted16th,
        /// <summary>
        /// Dotte d8th
        /// </summary>
        Dotted8th,
        /// <summary>
        /// Scottish 16th
        /// </summary>
        Scottish16th,
        /// <summary>
        /// Scottish 8th
        /// </summary>
        Scottish8th
    }
    // ReSharper restore InconsistentNaming
}